# variables.py - Learning Variables in Python

# Text (String)
name = "Alice"
city = "New York"

# Number (Integer)
age = 20
year = 2024

# Decimal (Float)
height = 5.6
weight = 55.5

# True/False (Boolean)
is_student = True
is_working = False

# Printing all variables
print("Name:", name)
print("City:", city)
print("Age:", age)
print("Year:", year)
print("Height:", height)
print("Weight:", weight)
print("Is Student:", is_student)
print("Is Working:", is_working)# variables.py - Learning Variables in Python

